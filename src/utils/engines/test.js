describe('Sort engine: ', () => {
  it('Sort by RATE works as expected', () => {
    expect(SortEngine.rate([{
      placeId: 31,
      rateReal: 0.5
    }, {
      placeId: 32,
      rateReal: 0.1
    }, {
      placeId: 33,
      rateReal: 1.0
    }, {
      placeId: 34,
      rateReal: 0.2
    }])).toEqual([{
      placeId: 32,
      rateReal: 0.1
    }, {
      placeId: 34,
      rateReal: 0.2
    }, {
      placeId: 31,
      rateReal: 0.5
    }, {
      placeId: 33,
      rateReal: 1.0
    }])
  })
  it('Sort by DISTANCE works as expected', () => {
    expect(SortEngine.distance([{
      placeId: 31,
      latitude: 0.5,
      longitude: 0.8
    }, {
      placeId: 32,
      latitude: 0.5,
      longitude: 0.7
    }, {
      placeId: 33,
      latitude: 0.5,
      longitude: 0.6
    }], {
      latitude: 0.5,
      longitude: 0.5
    })).toEqual([{
      distance: 11.119069268139768,
      placeId: 33,
      latitude: 0.5,
      longitude: 0.6
    }, {
      distance: 22.238138535634697,
      placeId: 32,
      latitude: 0.5,
      longitude: 0.7
    }, {
      distance: 33.35720780183998,
      placeId: 31,
      latitude: 0.5,
      longitude: 0.8
    }])
  })
})


